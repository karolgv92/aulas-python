# Tipos int e float
# int: números inteiros
# O tipo int representa números inteiros positivos ou negativos, sem casas decimais.
# int sem sinal (unsigned int): números inteiros positivos, sem casas decimais.

print(11)
print(-11)

# float: números com pontos flutuantes
# O tipo float representa números reais, com casas decimais.
# O tipo float pode ser representado de duas formas:
# 1. Notação decimal: 3.14, 0.123, 1.0, 10.0
# 2. Notação científica: 3.14e-2, 1.0e-3, 1.0e3, 1.0e+3
# float sem sinal (unsigned float): números reais positivos, com casas decimais.

print(3.14)
print(-3.14)

# A função type() retorna o tipo de um objeto.

print(type(11))
print(type(-11))
print(type(3.14))
print(type(-3.14))
