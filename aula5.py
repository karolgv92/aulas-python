# Tipo de dado bool (booleano)
# Ao questionar algo em um programa de computador, a resposta será sempre verdadeira ou falsa.
# Existem vários operadores relacionais que retornam um valor booleano.
# Dentre eles, temos: ==, !=, >, <, >=, <=
# Que questionam se dois valores são iguais, se são diferentes, se um é maior que o outro, se um é menor que o outro, se um é maior ou igual ao outro, se um é menor ou igual ao outro, respectivamente.
print(2 == 2)
print(2 != 2)
print(2 > 2)
print(2 < 2)
print(2 >= 2)
print(2 <= 2)
print(type(2 == 2))
